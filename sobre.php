          <center><h1>Sobre</h1>
          <hr width="60%"></center>
          <p class="sobre">Fundada em 2004 na cidade de Jundiaí (SP), a Confeitaria Alima busca renovar o mercado dos doces. Tudo começou quando o pequeno, porém sonhador, Felipe Rovigatti Delfino propôs a idéia para Igor Henrique de Abreu e Adryan Welke da Silva, que de começo recusaram, porém Felipe decidiu continuar sozinho.</p>
          <center>
          Temos uma foto de Felipe pesquisando melhorias para sua confeitaria:
          <br>
          <img src="https://media.discordapp.net/attachments/703303923087179869/914535297147076618/rFnmMCOlrp3nRZ98vT3Il40185Bzzoc1SFtX87zrXewFGJMp0aiKzFeeAciN2qFsXfh0S7bheNhEORzpB1nf1TtPjUtbtvateDgF.png?width=355&height=473" alt="Pesquisando para melhorar sua confeitaria">
          </center>
          <p class="sobre">Após começar a vender alguns doces em sua faculdade, Delfino começou a ficar conhecido pelos seus doces, o chamavam de Garoto Alima. Foi quando ele tentou pedir a ajuda de seus amigos novamente, que dessa vez, acreditaram em seu potencial e investiram em seu trabalho.</p>
          <p class="sobre">5 anos após abrirem a primeira loja física, conseguiram apresentar seus trabalhos no programa Shark Tank, onde foram aceitos com gratidão pela idéia de expandir para os EUA.</p>
          <p class="sobre">Hoje em dia, a Confeitaria Alima contém 1 estrela Michelin, 5 prêmios BAKER TOP. E seu fundador, Felipe, foi renomeado a estrela da confeitaria, reconhecido por mais de 25 países.</p>

